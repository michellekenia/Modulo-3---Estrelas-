package Lista_CriandoObjetos;

public class Bola {
    String tipoDeEsporte;
    String marca;
    String cor;
    double circunferencia;
    double velocidade;

    public Bola(String tipoDeEsporte, String marca, String cor, double circunferencia, double velocidade) {
    this.tipoDeEsporte = tipoDeEsporte;
    this.marca = marca;
    this.cor = cor;
    this.circunferencia = circunferencia;
    this.velocidade = velocidade ;

    }

}
