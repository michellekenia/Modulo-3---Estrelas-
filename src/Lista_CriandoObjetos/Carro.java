package Lista_CriandoObjetos;

public class Carro {
    String marca;
    String modelo;
    String cor;
    double potencia;
    double qtdDePortas;
    double valor;

    public Carro (String marca, String modelo, String cor, double potencia, double qtdDePortas, double valor) {
    this.marca = marca;
    this.modelo = modelo;
    this.cor = cor;
    this.potencia = potencia;
    this.qtdDePortas = qtdDePortas;
    this.valor = valor;
    }


}
