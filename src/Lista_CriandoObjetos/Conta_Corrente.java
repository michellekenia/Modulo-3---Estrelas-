package Lista_CriandoObjetos;

public class Conta_Corrente {
    String nome;
    int agencia;
    int conta;
    double saldo;

    public Conta_Corrente(String nome, int agencia, int conta, double saldo) {

        this.nome = nome;
        this.agencia = agencia;
        this.conta = conta;
        this.saldo = saldo;
    }

}

