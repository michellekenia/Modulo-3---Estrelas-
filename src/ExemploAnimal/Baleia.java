package ExemploAnimal;

public class Baleia {
    String nome;
    String tipo;
    String habitat;
    double peso;
    double comprimento;

    public Baleia(String nome, String tipo, String habitat, double comprimento, double peso) {

        this.nome = nome;
        this.tipo = tipo;
        this.habitat = habitat;
        this.peso = peso;
        this.comprimento = comprimento;

    }

}

