package ExemploAnimal;

public class Papagaio {
    String nome;
    String tipo;
    String habitat;
    double comprimento;
    double peso;

    public Papagaio(String nome, String tipo, String habitat, double comprimento, double peso) {
        this.nome = nome;
        this.tipo = tipo;
        this.habitat = habitat;
        this.comprimento = comprimento;
        this.peso = peso;

    }
}
