package ExemploAnimal;

public class Gato {
    String raça;
    String tipo;
    String habitat;
    double comprimento;
    double peso;

    public Gato(String raça, String tipo, String habitat, double comprimento, double peso) {
        this.raça = raça;
        this.tipo = tipo;
        this.habitat = habitat;
        this.comprimento = comprimento;
        this.peso = peso;
    }
}
