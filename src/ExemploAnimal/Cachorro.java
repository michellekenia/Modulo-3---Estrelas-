package ExemploAnimal;

public class Cachorro {
    String raça;
    String tipo;
    String habitat;
    double comprimento;
    double peso;

    public Cachorro (String raça, String tipo, String habitat, double comprimento, double peso) {
        this.raça = raça;
        this.tipo = tipo;
        this.habitat = habitat;
        this.comprimento = comprimento;
        this.peso = peso;


    }
}

